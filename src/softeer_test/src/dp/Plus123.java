package dp;

import java.util.Scanner;

public class Plus123 {

	public void plus123() {
		Scanner sc = new Scanner(System.in);
		int t = sc.nextInt();
		int[] n = new int[t];
		for (int i = 0; i < n.length; i++) {
			n[i] = sc.nextInt();
		}
		
		
	}
}
